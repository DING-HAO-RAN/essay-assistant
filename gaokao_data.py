def get_random_topic(year=None):
    """获取随机高考真题"""
    import random
    if year:
        if str(year) in GAOKAO_TOPICS:
            topics = GAOKAO_TOPICS[str(year)]
            key = random.choice(list(topics.keys()))
            return {"year": year, "paper": key, **topics[key]}
    else:
        year = random.choice(list(GAOKAO_TOPICS.keys()))
        topics = GAOKAO_TOPICS[year]
        key = random.choice(list(topics.keys()))
        return {"year": year, "paper": key, **topics[key]}

def get_topics_by_theme(theme):
    """根据主题查找相关真题"""
    results = []
    for year, papers in GAOKAO_TOPICS.items():
        for paper_name, paper_info in papers.items():
            if theme in paper_info.get("themes", []):
                results.append({
                    "year": year,
                    "paper": paper_name,
                    **paper_info
                })
    return results

def get_materials_by_theme(theme):
    """根据主题获取素材"""
    return THEME_MATERIALS.get(theme, {})

